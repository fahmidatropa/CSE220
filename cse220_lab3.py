def compress_matrix(mat):#mat=matrix
  #TO DO
  row,col=mat.shape
  #sum=0
  out=np.zeros((row//2,col//2),dtype=int)#new 2d array/matrix create. jekhane element sob=0
  #print(out) #print krle dkhbo sb elem=0
  outr=0
  for r in range(0,row,2):
    outc=0
    for c in range(0,col,2):
      sum=mat[r][c]+mat[r][c+1]+mat[r+1][c]+mat[r+1][c+1]
      out[outr][outc]=sum
      outc+=1
    outr+=1
  return out
